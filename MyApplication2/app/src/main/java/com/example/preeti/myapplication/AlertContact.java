public class AlertContact {

    public String name;
    public String relationType;
    public String email;
    public int phone;

    public AlertContact(){}

    public AlertContact(String name, String relationType, String email, int phone) {
        this.name = name;
        this.relationType = relationType;
        this.email = email;
        this.phone = phone;
    }

}
